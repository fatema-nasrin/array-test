//inch to feet//
function getFeet(inch){
    var feet = inch /12;
    return feet;
}
var result = getFeet(1)

console.log(result)


//feet to inch//
function getinch(ft){
    var inch=ft*12;
    return inch;
}
var result = getinch(10)

console.log(result)



//kilometer to meter//
function getmeter(km){
    var meter=km*1000;
    return meter;
}
var result = getmeter(10)

console.log(result)


